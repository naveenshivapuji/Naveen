package raghu.teaching;

public class TypeCoonvert {

	static void print(int a) {
		System.out.println("Print integer type ");
	}

	static void print(double d) {
		System.out.println("Print double type");
	}

	public static void main(String[] args) {

		System.out.println("MMS");
		char ch = 'A';
		print(20);
		print(ch);
		byte b = 23;
		print(b);
		short s = 35;
		print(s);
		float f = 45.5F;
		print(f);
		long l = 365;
		print(l);
		print(75.76);

	}

}
